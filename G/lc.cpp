class Solution {
public:
    int rob(vector<int>& nums) {
        nums.insert(nums.begin(),0);
        vector<vector<int>>d(nums.size(),vector<int>(2,0));
        for (int i = 1; i < nums.size(); ++i) {
            d[i][0]=max(d[i-1][1],d[i-1][0]);
            d[i][1]=d[i-1][0]+nums[i];
        }
        return max(d.back()[0],d.back()[1]);
    }
};
